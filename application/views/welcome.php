                <h2>makanan pokok</h2>
                <p>Bicara tentang panduan gizi seimbang, pertama-tama, bagi porsi piring makan menjadi dua bagian. Isi setengah piring makan dengan makanan pokok dan lauk-pauk.</p>
                <p>Dari porsi setengah piring tersebut, isi 2/3 bagian dengan karbohidrat sebagai sumber tenaga utama untuk beraktivitas. Sumber karbohidrat yang bisa dipilih adalah nasi merah, kentang, sagu, ubi jalar, jagung, singkong, gandum, mi, bihun, dan sebagainya.</p>
            
                <br>
                <div class="gambar protein"></div>
                <h2>lauk-pauk</h2>
                <p>Setelah sumber karbohidrat, isi 1/3 bagian lagi dengan lauk-pauk yang merupakan sumber protein, baik hewani maupun nabati.
                   Protein hewani meliputi aneka ikan, daging, atau olahan susu, sedangkan protein nabati meliputi tahu, tempe, dan kacang-kacangan.
                </p>
                <p>Batasi konsumsi daging merah atau daging olahan seperti sosis, kornet, bacon, dan sebagainya.</p>
            
                <br>
                <div class="gambar sayur_buah"></div>
                <h2>sayur & buah</h2>
                <p>Kalau setengah porsi piring sudah terisi dengan karbohidrat dan lauk-pauk, penuhi setengahnya dengan sayur dan buah.
                   Menurut data dari Badan Pusat Statistik, konsumsi sayur masyarakat Indonesia tergolong rendah, yaitu hanya 180 gram setiap harinya. Padahal, jumlah yang disarankan oleh Badan Kesehatan Dunia (WHO) adalah 400 gram.
                   Dengan panduan tersebut, Anda dianjurkan untuk mengisi 2/3 bagiannya dengan sayuran. Bisa dikonsumsi mentah, ditumis, dikukus, atau direbus.
                   Sayuran seperti kangkung, bayam, brokoli, buncis, terung, wortel, paprika, dan sayuran lainnya bisa dijadikan sumber serat, vitamin A, C, dan mineral seperti zat besi, fosfor, begitu juga antioksidan.
                </p>
                <p>Terakhir, jangan lupa tambahkan buah, yang sayangnya tingkat konsumsi masyarakat masih sangat rendah. Padahal, baik buah maupun sayur sangat penting untuk melengkapi kebutuhan serat, air, vitamin, mineral.
                   Isi 1/3 bagian piring yang tersisa dengan buah seperti pisang, semangka, melon, jeruk, apel, mangga, kiwi, stroberi, dan lainnya. Konsumsi sayur dan buah secara cukup setiap harinya dapat mencegah berbagai penyakit, seperti penyakit jantung, kanker, dan diabetes.
                </p>


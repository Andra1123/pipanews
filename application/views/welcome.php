                <h1>kriteria makanan sehat seperti apa sih ?</h1>
                <p>Kita dianjurkan makan sehat agar terhindar dari berbagai masalah kesehatan. Makanan sehat adalah makanan yang memberikan nutrisi yang kita butuhkan untuk menjaga kesehatan serta dapat memberikan rasa lebih baik pada tubuh , dan memberi energi. Nutrisi yang perlu diperhatikan antara lain: protein, karbohidrat, lemak, air, vitamin, dan mineral</p>
